package preparation;

public class swap {
	
		public static void main(String[] args) {
			
			
			int a=10;
			int b=20;
			int temp=0;
			
			temp=a;
			a=b;
			b=temp;
			
			System.out.println(a +" "+ b);
		
			System.out.println("****************************************************");
			
			int c=105;
			int d=280;
		
			c=c+d;
			d=c-d;
			c=c-d;
			
			System.out.println(c +" "+ d);
			
		}
	}


